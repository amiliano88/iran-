document.addEventListener('DOMContentLoaded', () => {
  const settingsBtn = document.querySelector('.settings-btn');
  if (settingsBtn) {
    settingsBtn.addEventListener('click', () => {
      alert('بخش تنظیمات در دست توسعه است! ⚙️');
    });
  }

  const triviaDisplay = document.getElementById('trivia-display');

  // تابع برای نمایش یک دانستنی تصادفی هنگام بارگذاری صفحه
  function showRandomTriviaOnLoad() {
    const randomIndex = Math.floor(Math.random() * trivias.length);
    triviaDisplay.querySelector('p').textContent = trivias[randomIndex];
  }

  // نمایش دانستنی تصادفی بلافاصله پس از بارگذاری DOM
  showRandomTriviaOnLoad();
});